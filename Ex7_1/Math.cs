using System;

namespace Ex7_1
{
    public class Math
    {
        public double Add(double op1, double op2)
        {
            return op1 + op2;
        }

        public double Subtract(double op1, double op2)
        {
            return op1 - op2;
        }

        public double Multiply(double op1, double op2)
        {
            return op1 * op2;
        }

        public double Divide(double op1, double op2)
        {
            return op1 / op2;
        }
    }
}