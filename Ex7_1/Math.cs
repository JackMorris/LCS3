using System;

namespace Ex7_1
{
    public class Math
    {
        public double Add(double v1, double v2)
        {
            return v1 + v2;
        }

        public double Subtract(double v1, double v2)
        {
            return v1 - v2;
        }

        public double Multiply(double v1, double v2)
        {
            return v1 * v2;
        }

        public double Divide(double v1, double v2)
        {
            return v1 / v2;
        }
    }
}